# =====================================================================================
# DTO(Data Transfer Object) 파일입니다.
# "API가 주고받는 데이터의 모양(형식)"을 정의합니다.
#  - Response(응답) DTO : 서버가 클라이언트(안드로이드 앱)로 돌려주는 JSON의 형태
# =====================================================================================

# date : "오늘의 팁"이 어느 날짜(KST) 기준인지 함께 내려주기 위한 타입.
#        datetime과 달리 date는 오프셋 없는 "YYYY-MM-DD"로 직렬화됩니다(명세 규격).
from datetime import date

# BaseSerializerModel : 우리 프로젝트 공통 응답 베이스(from_attributes=True).
#   → 정적 카탈로그의 DailyTip 객체(속성 보유)를 곧바로 이 응답 DTO로 변환할 수 있습니다.
from app.dtos.base import BaseSerializerModel


# -------------------------------------------------------------------------------------
# [응답] GET /daily-tips 응답. 화면 `_11`의 '오늘의 팁'에 1:1로 뿌립니다.
# 하루 1건(오늘의 팁)만 내려주므로 배열로 감싸지 않고 단건 형태로 반환합니다.
#   예: { "date": "2026-07-14", "tip_id": 3, "category": "영양",
#         "title": "끼니마다 단백질 한 가지", "body": "..." }
# -------------------------------------------------------------------------------------
class DailyTipResponse(BaseSerializerModel):
    tip_date: date  # 이 팁이 노출되는 날짜(KST 오늘). 앱이 "오늘" 캐싱/갱신 판단에 활용
    tip_id: int  # 팁 식별자(카탈로그 내 고정 번호)
    category: str  # 분류 라벨(예: "걷기", "근력", "영양")
    title: str  # 화면에 보여줄 제목(바로 뿌릴 문자열)
    body: str  # 화면에 보여줄 본문(바로 뿌릴 문자열)
