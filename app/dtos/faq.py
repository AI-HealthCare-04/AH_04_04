# =====================================================================================
# DTO(Data Transfer Object) 파일입니다.
# "API가 주고받는 데이터의 모양(형식)"을 정의합니다.
#  - Response(응답) DTO : 서버가 클라이언트(안드로이드 앱)로 돌려주는 JSON의 형태
# =====================================================================================

from pydantic import BaseModel

from app.dtos.base import BaseSerializerModel


# -------------------------------------------------------------------------------------
# [응답] GET /support/faqs 의 FAQ 한 건. 고객센터 화면의 Q&A 항목 1개에 대응.
# -------------------------------------------------------------------------------------
class FaqItemResponse(BaseSerializerModel):
    faq_id: int  # 목록 내 고정 번호(정렬·식별용)
    question: str  # 질문(화면 표시)
    answer: str  # 답변(화면 표시)


# -------------------------------------------------------------------------------------
# [응답] GET /support/faqs 전체. FAQ 여러 건을 "faqs" 배열로 감쌉니다.
#   예: { "faqs": [ {faq_id, question, answer}, ... ] }
# -------------------------------------------------------------------------------------
class FaqListResponse(BaseModel):
    faqs: list[FaqItemResponse]
