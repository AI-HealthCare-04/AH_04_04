# 조합형 랜덤 닉네임 — "형용사 + 명사"로 기본 표시명을 만든다(예: "슬기로운 두루미").
#   - 게스트 로그인의 기본 닉네임 생성에 사용(고정 "체험하는분" 대체).
#   - 노인 대상 서비스라 따뜻하고 친숙한 자연/동물 + 긍정 형용사로 구성한다.
#   - nickname 컬럼은 유니크 제약이 없어 중복돼도 무방하다. 접미사(숫자)는 붙이지 않는다.
#     사용자는 이후 PATCH /users/me로 바꿀 수 있으므로 "기본 표시명" 역할만 한다.
#   - 재사용 가능(향후 소셜 로그인 기본 닉네임 등)하도록 순수 함수로 둔다.
# =====================================================================================
import random

# 긍정·따뜻한 형용사(30)
_ADJECTIVES: tuple[str, ...] = (
    "슬기로운", "씩씩한", "튼튼한", "다정한", "부지런한", "든든한", "상냥한", "너그러운",
    "활기찬", "포근한", "정다운", "온화한", "유쾌한", "명랑한", "싱그러운", "반가운",
    "넉넉한", "참한", "어진", "곧은", "정직한", "용감한", "따사로운", "믿음직한",
    "느긋한", "환한", "고운", "야무진", "화사한", "빛나는",
)

# 친숙한 자연·동물 명사(30)
_NOUNS: tuple[str, ...] = (
    "호랑이", "두루미", "소나무", "청설모", "진달래", "고래", "다람쥐", "기러기",
    "은행나무", "무궁화", "제비", "잉어", "사슴", "토끼", "부엉이", "참새",
    "백로", "동백꽃", "국화", "매화", "나비", "반딧불이", "거북이", "여우",
    "수달", "물총새", "황새", "도라지", "고슴도치", "청둥오리",
)


def generate_nickname() -> str:
    """조합형 랜덤 닉네임 하나를 만든다(예: "슬기로운 두루미"). 길이는 최대 12자 안팎이라 50자 제한 안전."""
    return f"{random.choice(_ADJECTIVES)} {random.choice(_NOUNS)}"
